from __future__ import annotations

import argparse
import ast
import csv
import json
from pathlib import Path
from typing import Any


def _read_summary(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    with path.open("r", encoding="utf-8", newline="") as f:
        rows = list(csv.DictReader(f))
    return rows[0] if rows else {}


def _parse_metrics(log_path: Path) -> dict[str, Any]:
    if not log_path.exists():
        return {}
    lines = log_path.read_text(encoding="utf-8", errors="replace").splitlines()
    for line in reversed(lines):
        stripped = line.strip()
        if stripped.startswith("{") and stripped.endswith("}"):
            try:
                return ast.literal_eval(stripped.replace("np.float64(", "").replace(")", ""))
            except Exception:
                return {"raw_metrics": stripped}
    return {}


def main() -> None:
    parser = argparse.ArgumentParser(description="Summarize experiment logs and missing-data reports.")
    parser.add_argument("--name", required=True, help="Experiment name, for example smoke or week.")
    parser.add_argument("--log", required=True, type=Path, help="Experiment run log path.")
    parser.add_argument("--missing-summary", required=True, type=Path, help="Missing-data summary CSV path.")
    parser.add_argument("--metrics-json", type=Path, default=None, help="Metrics JSON path generated by run_experiment.py.")
    args = parser.parse_args()

    summary = _read_summary(args.missing_summary)
    if args.metrics_json and args.metrics_json.exists():
        payload = json.loads(args.metrics_json.read_text(encoding="utf-8"))
        metrics = payload.get("metrics", {})
    else:
        metrics = _parse_metrics(args.log)

    print(f"experiment={args.name}")
    if summary:
        print("data:")
        for key in [
            "candidate_rows",
            "daily_limit_candidates",
            "events_built",
            "missing_tick",
            "missing_minute",
            "missing_l2",
            "empty_l2_event_day",
        ]:
            if key in summary:
                print(f"  {key}: {summary[key]}")
    else:
        print("data: missing summary not found")

    if metrics:
        print("metrics:")
        for key, value in metrics.items():
            print(f"  {key}: {value}")
    else:
        print("metrics: not found")


if __name__ == "__main__":
    main()
